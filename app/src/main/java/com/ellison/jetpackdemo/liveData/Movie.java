package com.ellison.jetpackdemo.liveData;

public class Movie {
    String name;
    String type;
    String actor;
    int postID;

    public Movie(String name, String type, String actor, int postID) {
        this.name = name;
        this.type = type;
        this.actor = actor;
        this.postID = postID;
    }
}
